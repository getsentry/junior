#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parseArgs } from "node:util";

const packageRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  ".."
);

const { values, positionals } = parseArgs({
  options: {
    home: { type: "string" },
    port: { type: "string", short: "p" }
  },
  allowPositionals: true,
  strict: false
});

const command = positionals[0];

// ---------------------------------------------------------------------------
// junior init <dir>
// ---------------------------------------------------------------------------
if (command === "init") {
  const dir = positionals[1];
  if (!dir) {
    console.error("usage: junior init <dir>");
    process.exit(1);
  }

  const target = path.resolve(dir);
  fs.mkdirSync(target, { recursive: true });

  const name = path.basename(target);

  // package.json
  const pkg = {
    name,
    version: "0.1.0",
    private: true,
    type: "module",
    scripts: {
      dev: "junior dev",
      build: "junior build",
      start: "junior start"
    },
    dependencies: {
      junior: "latest",
      next: "^16.0.0",
      react: "^19.0.0",
      "react-dom": "^19.0.0",
      "@sentry/nextjs": "^10.0.0"
    }
  };
  fs.writeFileSync(
    path.join(target, "package.json"),
    JSON.stringify(pkg, null, 2) + "\n"
  );

  // config.toml
  fs.writeFileSync(
    path.join(target, "config.toml"),
    `[bot]\nname = "${name}"\n\n[ai]\nmodel = "anthropic/claude-sonnet-4.6"\nfast_model = "anthropic/claude-haiku-4-5"\n`
  );

  // SOUL.md
  fs.writeFileSync(
    path.join(target, "SOUL.md"),
    `# ${name}\n\nYou are ${name}, a helpful assistant.\n`
  );

  // skills/
  const skillsDir = path.join(target, "skills");
  fs.mkdirSync(skillsDir, { recursive: true });
  fs.writeFileSync(path.join(skillsDir, ".gitkeep"), "");

  // .gitignore
  fs.writeFileSync(
    path.join(target, ".gitignore"),
    [
      "node_modules/",
      ".next/",
      ".env",
      ".env.local",
      "",
      "# Generated by junior",
      "app/",
      "next.config.ts",
      "instrumentation.ts",
      ""
    ].join("\n")
  );

  // .env.example
  fs.writeFileSync(
    path.join(target, ".env.example"),
    [
      "SLACK_BOT_TOKEN=",
      "REDIS_URL=",
      "NEXT_PUBLIC_SENTRY_DSN=",
      "SENTRY_ORG=",
      "SENTRY_PROJECT=",
      "SENTRY_AUTH_TOKEN=",
      ""
    ].join("\n")
  );

  console.log(`Created ${name} at ${target}`);
  console.log();
  console.log(`  cd ${dir} && pnpm install && pnpm dev`);
  console.log();
  process.exit(0);
}

// ---------------------------------------------------------------------------
// Shim generation (used by dev + build)
// ---------------------------------------------------------------------------
const GENERATED_HEADER = "// @generated by junior — do not edit\n";

function generateShim(targetDir) {
  // app/api/[...path]/route.ts
  const routeDir = path.join(targetDir, "app", "api", "[...path]");
  fs.mkdirSync(routeDir, { recursive: true });
  fs.writeFileSync(
    path.join(routeDir, "route.ts"),
    GENERATED_HEADER +
      'export { GET, POST, runtime } from "junior/handler";\n'
  );

  // app/layout.tsx
  fs.mkdirSync(path.join(targetDir, "app"), { recursive: true });
  fs.writeFileSync(
    path.join(targetDir, "app", "layout.tsx"),
    GENERATED_HEADER +
      'export { default } from "junior/app/layout";\n'
  );

  // next.config.ts
  fs.writeFileSync(
    path.join(targetDir, "next.config.ts"),
    GENERATED_HEADER +
      'import { withJunior } from "junior/config";\n\nexport default withJunior();\n'
  );

  // instrumentation.ts
  fs.writeFileSync(
    path.join(targetDir, "instrumentation.ts"),
    GENERATED_HEADER +
      'export { register, onRequestError } from "junior/instrumentation";\n'
  );
}

// ---------------------------------------------------------------------------
// junior dev
// ---------------------------------------------------------------------------
if (command === "dev") {
  const homeDir = path.resolve(values.home ?? process.cwd());
  generateShim(homeDir);

  const args = ["next", "dev"];
  if (values.port) args.push("-p", values.port);

  execFileSync("npx", args, {
    cwd: homeDir,
    stdio: "inherit",
    env: { ...process.env, JUNIOR_HOME: homeDir }
  });
  process.exit(0);
}

// ---------------------------------------------------------------------------
// junior build
// ---------------------------------------------------------------------------
if (command === "build") {
  const homeDir = path.resolve(values.home ?? process.cwd());
  generateShim(homeDir);

  execFileSync("npx", ["next", "build"], {
    cwd: homeDir,
    stdio: "inherit",
    env: { ...process.env, JUNIOR_HOME: homeDir }
  });
  process.exit(0);
}

// ---------------------------------------------------------------------------
// junior start
// ---------------------------------------------------------------------------
if (command === "start") {
  const homeDir = path.resolve(values.home ?? process.cwd());

  const args = ["next", "start"];
  if (values.port) args.push("-p", values.port);

  execFileSync("npx", args, {
    cwd: homeDir,
    stdio: "inherit",
    env: { ...process.env, JUNIOR_HOME: homeDir }
  });
  process.exit(0);
}

// ---------------------------------------------------------------------------
// Legacy: no subcommand — run `next start` from package root
// ---------------------------------------------------------------------------
const homeDir = path.resolve(values.home ?? process.cwd());

if (!fs.existsSync(path.join(homeDir, "SOUL.md"))) {
  console.error(`error: SOUL.md not found in home directory: ${homeDir}`);
  process.exit(1);
}

const args = ["next", "start"];
if (values.port) args.push("-p", values.port);

execFileSync("npx", args, {
  cwd: packageRoot,
  stdio: "inherit",
  env: { ...process.env, JUNIOR_HOME: homeDir }
});
